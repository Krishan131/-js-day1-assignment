// setup.js
// This file lists all the steps to set up GitHub, create a repository,
// and configure VS Code + Node.js environment.

// =============================
// 1. INSTALL NODE.JS
// =============================
// - Visit: https://nodejs.org
// - Download LTS version.
// - Install normally and verify using:
//      node -v
//      npm -v

// =============================
// 2. INSTALL VS CODE
// =============================
// - Visit: https://code.visualstudio.com
// - Download and install.
// - Install recommended extensions:
//      * JavaScript/TypeScript Language Features
//      * GitHub Pull Requests & Issues
//      * GitLens
//      * Prettier

// =============================
// 3. CREATE GITHUB ACCOUNT
// =============================
// - Visit: https://github.com
// - Click Sign Up.
// - Enter email, password, username.
// - Verify account.
// - Log in.

// =============================
// 4. CREATE NEW GITHUB REPOSITORY
// =============================
// - After logging in, click your "+" icon → New Repository.
// - Enter repository name.
// - Choose public or private.
// - Add README (optional).
// - Click Create Repository.
// - Copy the repo URL (HTTPS preferred).

// =============================
// 5. CLONE REPOSITORY USING VS CODE
// =============================
// - Open VS Code.
// - Press Ctrl+Shift+P → type "Git: Clone".
// - Paste your GitHub repo URL.
// - Choose a folder to save.
// - VS Code will open the cloned project automatically.

// =============================
// 6. INITIALIZE NODE PROJECT
// =============================
// - Open terminal inside VS Code.
// - Run:
//      npm init -y
// - This creates package.json.

// =============================
// 7. CREATE PROJECT FILES
// =============================
// - Create index.js
// - Write a simple script:
//      console.log("Project setup successful!");

// =============================
// 8. INSTALL COMMON PACKAGES (OPTIONAL)
// =============================
// - Examples:
//      npm install express
//      npm install nodemon --save-dev

// =============================
// 9. RUN PROJECT
// =============================
// - Use:
//      node index.js
// - Or if using nodemon:
//      npx nodemon index.js

// =============================
// 10. COMMIT CHANGES
// =============================
// - In VS Code source control:
//      * Stage changes.
//      * Write commit message.
//      * Click Commit.

// =============================
// 11. PUSH TO GITHUB
// =============================
// - Click "Sync Changes" or "Push".
// - Your code will appear on your GitHub repo.

// =============================
// 12. DONE
// =============================
// Your GitHub + VS Code + Node.js setup is complete.
